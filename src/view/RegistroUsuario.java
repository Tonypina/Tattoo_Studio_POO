    private boolean contraseñaCorrecta ( char[]j, char[]h){
        boolean valor = true;
        int puntero = 0;
        if (j.length != h.length){
            valor =false;
        }else{
            while ((valor)&&(puntero < j.length  ) ){
             if (j[puntero] != h[puntero]){
                 valor =false;
             }
             puntero++;
            }
        }
    return valor;
    }
