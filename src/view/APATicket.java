    private void LlenarNombresProv(ArrayList<Proveedor> lista){
        for(Proveedor prov: lista)
        {
            String nameprov = prov.getNombre();
            proveedores.addItem(nameprov);
        }
    }
    private void LlenarTablaProd(ArrayList<Producto> listp){
       DefaultTableModel modelo = new DefaultTableModel();
        tabla.setModel(modelo);
        int cantidadColumnas = 5;
          
        modelo.addColumn("ID");
        modelo.addColumn("Modelo");
        modelo.addColumn("Tipo");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio");  
        int[] anchos ={40,70,70,40,70};
          
        for (int x =0; x < cantidadColumnas; x++  ){
            tabla.getColumnModel().getColumn(x).setPreferredWidth(anchos[x]);
        }
        
        for(Producto p : listp){
            Object[] fila = new Object[cantidadColumnas];
            fila[0] = p.getIdPro();
            fila[1] = p.getModeloPro();
            fila[2] = p.getTipoPro();
            fila[3] = p.getCantidadPro();
            fila[4] = p.getPrecioPro();
            modelo.addRow(fila);
        }
    }
