    private ArrayList<Proveedor> proveedores(){
        ArrayList<Proveedor> pl = new ArrayList<>();
        for(Proveedor p : Model.getProveedor()){
            if(!p.getNombre().endsWith("-P")){    
                proveedores.addItem(p.getNombre());
                pl.add(p);
            }
        }
        return pl;
    }
    
    private void llenarTablaProductos(ArrayList<Producto> lista){
        DefaultTableModel modelo = new DefaultTableModel();
        tabla.setModel(modelo);
        int cantidadColumnas = 6;
          
        modelo.addColumn("ID");
        modelo.addColumn("Modelo");
        modelo.addColumn("Tipo");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Costo");
        modelo.addColumn("Precio");  
        int[] anchos ={40,70,70,90,80,80};
          
        for (int x =0; x < cantidadColumnas; x++  ){
            tabla.getColumnModel().getColumn(x).setPreferredWidth(anchos[x]);
        }
        
        for( Producto p : lista){
            Object[] fila = new Object[cantidadColumnas];
            fila[0] = p.getIdPro();
            fila[1] = p.getModeloPro();
            if(p.isPerfo())
                fila[2] = "PerforaciÃ³n";
            else
                fila[2] = p.getTipoPro();
            fila[3] = p.getCantidadPro();
            fila[4] = p.getCostoPro(); 
            fila[5] = p.getPrecioPro();
            modelo.addRow(fila);
        }
    }
      
