private void LlenarTablaProd(ArrayList<Producto> listp){
       DefaultTableModel modelo = new DefaultTableModel();
        jTable2.setModel(modelo);
        int cantidadColumnas = 5;
        modelo.addColumn("ID");
        modelo.addColumn("Modelo");
        modelo.addColumn("Tipo");
        modelo.addColumn("Cantidad Disponible");
        modelo.addColumn("Precio");  
        int[] anchos ={40,70,70,100,70};

        for (int x =0; x < cantidadColumnas; x++  ){
            jTable2.getColumnModel().getColumn(x).setPreferredWidth(anchos[x]);
        }

        for(Producto p : listp){
            Object[] fila = new Object[cantidadColumnas];
            fila[0] = p.getIdPro();
            fila[1] = p.getModeloPro();
            fila[2] = p.getTipoPro();
            fila[3] = p.getCantidadPro();
            fila[4] = p.getPrecioPro();
            modelo.addRow(fila);
        }
    }
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int fsel = jTable2.getSelectedRow();
        String prov = proveedores.getSelectedItem().toString();
        try{
            String Model0,Tipo,precio,Id,Cant,producto;
            if(fsel==-1){
                JOptionPane.showMessageDialog(null, "No ha seleccionado ningún producto" );
            }else{
                if(prov=="Seleccionar Proveedor"){
                    JOptionPane.showMessageDialog(null, "No ha seleccionado ningún proveedor, intentar nuevamente" );
                }else{
                    Producto p = Model.buscarProducto(Integer.parseInt(jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString()));
                    m = (DefaultTableModel) jTable2.getModel();
                    Model0 = jLabel20.getText();
                    Tipo = jLabel21.getText();
                    precio = jLabel22.getText();
                    Id = jLabel19.getText();
                    Cant = jTextField7.getText();
                    int CantSelec = Integer.parseInt(Cant);
                    double PrecioBase = Double.parseDouble(precio);
                    if(CantSelec>p.getCantidadPro()){
                        JOptionPane.showMessageDialog(null, "La cantidad de productos solicitados es mayor a la que se encuentra en Stock" );
                    }else{
                        double Preciototal = CantSelec*PrecioBase;
                        producto = Tipo + " " + Model0;
                        m = (DefaultTableModel) jTable1.getModel();
                        String filaelemento[]= {prov,Id,Cant,producto,Double.toString(Preciototal)};
                        m.addRow(filaelemento);
                        int CantStock = p.getCantidadPro();
                        int cantAct = CantStock - Integer.parseInt(Cant);
                        int id = p.getIdPro();
                        Model.actualizarStock(id, cantAct);
                        JOptionPane.showMessageDialog(null, "Se agregará el producto seleccionado." );
                    }
                }
            }
        }catch(Exception e){
        }
        jDialog1.setVisible(false); 
    }//GEN-LAST:event_jButton4ActionPerformed

